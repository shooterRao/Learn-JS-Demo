function abstractTableData(data) {
  const columnsData = [
    {
      title: "区域",
      key: "regionName"
    }
  ];
  const tableData = [];
  const columnsMap = {};
  const regionMap = {};

  data.forEach(v => {
    const { regionName, year, value } = v;
    if (!columnsMap[year]) {
      columnsMap[year] = true;
      const obj = {
        title: year,
        key: year
      };
      columnsData.push(obj);
    }
    if (!regionMap[regionName]) {
      regionMap[regionName] = true;
      tableData.push({
        regionName,
        [year]: value
      });
    } else {
      const o = tableData.find(v => regionName === v.regionName);
      o[year] = value;
    }
  })
  // 返回一个对象
  return {
    columnsData,
    tableData
  }
}
